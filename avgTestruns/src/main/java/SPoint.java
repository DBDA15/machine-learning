
public class SPoint {
	private int gen;
	private int perc;
	private int ms;
	
	
	
	public SPoint(int gen, int ms, int perc) {
		super();
		this.gen = gen;
		this.perc = perc;
		this.ms = ms;
	}
	public int getGen() {
		return gen;
	}
	public void setGen(int gen) {
		this.gen = gen;
	}
	public int getPerc() {
		return perc;
	}
	public void setPerc(int perc) {
		this.perc = perc;
	}
	public int getMs() {
		return ms;
	}
	public void setMs(int ms) {
		this.ms = ms;
	}

}
